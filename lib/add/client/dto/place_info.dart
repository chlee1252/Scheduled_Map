class PlaceInfo {
  late String _addressName;
  late String _categoryGroupCode;
  late String _categoryGroupName;
  late String _categoryName;
  late String _distance;
  late String _id;
  late String _phone;
  late String _placeName;
  late String _placeUrl;
  late String _roadAddressName;
  late String _x;
  late String _y;

  String get addressName => _addressName;

  set addressName(String value) {
    _addressName = value;
  }

  String get y => _y;

  set y(String value) {
    _y = value;
  }

  String get x => _x;

  set x(String value) {
    _x = value;
  }

  String get roadAddressName => _roadAddressName;

  set roadAddressName(String value) {
    _roadAddressName = value;
  }

  String get placeUrl => _placeUrl;

  set placeUrl(String value) {
    _placeUrl = value;
  }

  String get placeName => _placeName;

  set placeName(String value) {
    _placeName = value;
  }

  String get phone => _phone;

  set phone(String value) {
    _phone = value;
  }

  String get id => _id;

  set id(String value) {
    _id = value;
  }

  String get distance => _distance;

  set distance(String value) {
    _distance = value;
  }

  String get categoryName => _categoryName;

  set categoryName(String value) {
    _categoryName = value;
  }

  String get categoryGroupName => _categoryGroupName;

  set categoryGroupName(String value) {
    _categoryGroupName = value;
  }

  String get categoryGroupCode => _categoryGroupCode;

  set categoryGroupCode(String value) {
    _categoryGroupCode = value;
  }
}
